					<h2 id="Le club de St Avertin CASAS inaugure sa nouvelle salle dédiée au tir à l'arc">Le club de St Avertin CASAS inaugure sa nouvelle salle dédiée au tir à l'arc</h2>
					<article>
						<h4>Article publié le 06/07/2021</h4>
						<p>
							Bien connu des passionnés de tir à l'arc, le club de St Avertin CASAS, hôte de nombreuses manifestations nationales vient d'inaugurer le samedi 3 juillet dernier sa nouvelle salle dédiée entièrement au tir à l'arc. Un projet soutenu par la fédération au travers de son plan d'équipements "50 salles pour tirer à l'arc".
						</p>
						<p>
							Ce samedi 3 juillet 2021 est devenue une nouvelle date clé dans l’histoire du club de St Avertin (37). Aux côtés des représentants institutionnels locaux, départementaux, régionaux ainsi que des représentants de la FFTA, avait lieu l’inauguration du nouveau complexe de tir à l’arc. Cet espace de 1600m2 d’un budget de 2,7M est devenu réalité. Une salle moderne qui portera le nom du président emblématique du club, Gaétan Robin, qui doit laisser sa place dans quelques jours après 35 ans d’exercice. Cela sera toujours avec rigueur, travail, sérieux et humilité, comme il le fait depuis sa création, que le club va poursuivre son développement autour de ce nouveau lieu de pratique. 
						</p>
						<p>
							Hier après-midi, le para-archer classique basque a réalisé de très beaux tirs sur le terrain de République Tchèque. Enchainant les victoires, l’athlète qui tire avec sa bouche remporte le tournoi européen de qualification paralympiques.
						</p>
						<p>
							L’équipement va permettre d’accroitre la diversité des pratiques et des publics accueillis. Reconnu pour son engagement auprès des nombreux publics comme la pratique handi, des seniors, le sport santé, de nouvelles actions comme celles auprès des scolaires devraient également trouver leur place au sein de la structure.
						</p>
						<p>
							De l’animation, à la pratique des adhérents en passant par l’entrainement de haut niveau, mais aussi la compétition, ou encore des stages, sans aucun doute l’espace apparaitra à nouveau rapidement exiguë face aux ambitions du club. 
						</p>
						<p>
							Cette nouvelle salle, construite à proximité du terrain extérieur du club, compte 25 cibles intérieures et des fenêtres permettant de tirer aux distances extérieures toute l'année. Cet équipement, l'un des plus grands du territoire national, se veut moderne dans sa conception en prenant en compte l’environnement et la réduction des coûts d’entretien. Il sera par ailleurs doté de panneaux solaires et d'un système de chauffage adapté.
						</p>
