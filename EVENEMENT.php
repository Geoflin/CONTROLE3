					<h2 id="Nouveaux records de France en cadette et junior femmes">Nouveaux records de France en cadette et junior femmes</h2>
					<article>
						<h4>Article publié le 09/07/2021</h4>
						<p>
							Le niveau du tir à l'arc féminin n'en finit plus de progresser, ce mercredi 7 juillet deux nouveaux records de France on été battus dans les catégories jeunes. Mélodie Richard et Caroline Lopez ont amélioré la barre en cadette et junior femme.
						</p>
						<p>
							Plusieurs belles performances ont émaillé la compétition organisée ce mercredi 7 juillet au Creps de Dijon. Parmi les résultats, deux nouveaux records de France officialisés cette fin de semaine par la fédération.
						</p>
						<p>
							En cadette, Caroline Lopez du club de Monchy au Bois réalise 685 points (60m) et bat de 9 unités le record de France de la catégorie. Un record jusque là détenue par Mélanie Gaubil avec 676 points.
						</p>
						<p>
							Chez les juniors femmes, le record était détenu par Audrey Adiceom avec 667 points. Une performance améliorée de deux points par Mélodie Richard. L'archère d'Issy-Les-Moulineaux porte ainsi la marque à 669 points.
						</p>
						<p>
							Comme Mélanie Gaubil et Audrey Adiceom, toutes deux membres de l'équipe de France, les deux athlètes peuvent rêver grand dans les prochaines années et peut-être des Jeux de Paris en 2024.
						</p>
					</article>
					<h2 id="Daniel Lelou et Guillaume Toucoullet décrochent leur quota pour les Jeux Paralympiques de Tokyo !">Daniel Lelou et Guillaume Toucoullet décrochent leur quota pour les Jeux Paralympiques de Tokyo !</h2>
					<article>
						<h4>Article publié le 08/07/2021</h4>
						<p>
							Les archers handisport se sont affrontés cette semaine à l'occasion du tournoi de qualification mondial et continental pour les Jeux paralympiques de Tokyo à Nove-Mesto en République Tchèque. Guillaume Toucoullet et Daniel Lelou ont remporté ce mercredi 7 et jeudi 8 juillet leur quota pour les Jeux suite à de très belles performances. Une excellente nouvelle pour l'équipe de France qui devrait ainsi compter quatre représentants au Japon.
						</p>
						<p>
							La France rempli son équipe d’archers pour les Jeux paralympiques ! Après Julie Chupin et Eric Pereira qui ont obtenu leurs quotas en arc à poulies open, ce sont Daniel Lelou et Guillaume Toucoullet qui les les ont imités. Ces résultats permettent à l'équipe de France para-archerie de disposer de 4 quotas pour Tokyo 2020 (2 Open poulies hommes, 1 Open poulies femme, 1 Open classique homme).
						</p>
						<p>
							Hier après-midi, le para-archer classique basque a réalisé de très beaux tirs sur le terrain de République Tchèque. Enchainant les victoires, l’athlète qui tire avec sa bouche remporte le tournoi européen de qualification paralympiques.
						</p>
						<p>
							En arc à poulies, Daniel Lelou s’est largement imposé face à ses adversaires, remportant ses quarts de finale et sa demi-finale aisément, il termine à la seconde place, médaillé d’argent et avec son ticket d’entrée pour le Japon.
						</p>
						<p>
							L'équipe tricolore devra être confirmée par la commission du CPSF avant de disputer leurs épreuves du 27 août au 4 septembre à Tokyo, dans l’espoir de remporter une médaille.
						</p>
